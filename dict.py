#keys(): Возвращает все ключи словаря.

my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}
all_keys = my_dict.keys()  # all_keys = ['name', 'age', 'city']


#values(): Возвращает все значения словаря.

data = {'x': 10, 'y': 20, 'z': 30}
all_values = data.values()  # all_values = [10, 20, 30]


#items(): Возвращает все пары ключ-значение словаря в виде кортежей.

my_dict = {'name': 'Alice', 'age': 30, 'city': 'Paris'}
all_items = my_dict.items()  # all_items = [('name', 'Alice'), ('age', 30), ('city', 'Paris')]


#get(): Возвращает значение по ключу; если ключ отсутствует, возвращает указанное значение по умолчанию.

person = {'name': 'Bob', 'age': 25}
age = person.get('age', 0)  # age = 25
height = person.get('height', 180)  # height = 180


#pop(): Удаляет элемент по ключу и возвращает его значение.

data = {'a': 1, 'b': 2, 'c': 3}
value_b = data.pop('b')  # value_b = 2, data = {'a': 1, 'c': 3}


#update(): Добавляет пары ключ-значение из другого словаря.

my_dict = {'name': 'Alice', 'age': 25}
my_dict.update({'city': 'New York', 'gender': 'female'})  # my_dict = {'name': 'Alice', 'age': 25, 'city': 'New York', 'gender': 'female'}


#setdefault(): Устанавливает значение по ключу, если ключ отсутствует.

user_info = {'name': 'John', 'age': 30}
user_info.setdefault('city', 'Unknown')  # user_info = {'name': 'John', 'age': 30, 'city': 'Unknown'}









